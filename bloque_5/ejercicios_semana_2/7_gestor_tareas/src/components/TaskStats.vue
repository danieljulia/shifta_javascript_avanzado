<script>
export default {
  name: 'TaskStats',
  props: {
    tasks: {
      type: Array,
      required: true
    }
  },
  computed: {
    completedCount() {
      return this.tasks.filter(t => t.completed).length
    },
    pendingCount() {
      return this.tasks.filter(t => !t.completed).length
    }
  }
}
</script>
